package com.tk.code.smartfiredetect.Model;

public class Company {
    private String companyName;
    private String companyPhone;
    private String companyRoomNumber;

    public Company() {
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getCompanyPhone() {
        return companyPhone;
    }

    public void setCompanyPhone(String companyPhone) {
        this.companyPhone = companyPhone;
    }

    public String getCompanyRoomNumber() {
        return companyRoomNumber;
    }

    public void setCompanyRoomNumber(String companyRoomNumber) {
        this.companyRoomNumber = companyRoomNumber;
    }
}
